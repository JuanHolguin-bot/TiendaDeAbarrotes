    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String username = txtUser.getText().trim();
        String password = new String(txtPassword.getPassword()).trim();

        if (username.equals("") || password.equals("")) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese usuario y contrase침a", "Login alert", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                boolean userValid = false;
                try {
                    userValid = serviceLogin.ValidateUserAndPassword(username, password);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Usuario o contrase침a incorrectos", "Login alert", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                gestioninventario.Service.IProductoManager productoManager = new gestioninventario.Service.GestorProductos();
                gestioninventario.Service.IStockManager stockManager = new gestioninventario.Service.GestorStock();
                ListaProductos adminSet = new ListaProductos(productoManager, stockManager, username);

                adminSet.setVisible(true);
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "Ocurri칩 un error inesperado: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

